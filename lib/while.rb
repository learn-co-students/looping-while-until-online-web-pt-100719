def using_while
  levitation_force = 6 # Declares the variable and sets its value
	
  while levitation_force < 10 #Begins our loop, brings in the value of our variable and sets a 
                              # condition
    puts "Wingardium Leviosa" # Outputs the string "Wingardium Leviosa"
